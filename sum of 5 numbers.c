//sum of five numbers
#include<stdio.h>

int main()
{
int f,g,h,i,j;
    printf("Enter the sum of five numbers\n");
    scanf("the first number %d",&f);
    scanf("the second number %d",&g);
    scanf("the third number %d",&h);
    scanf("the fourth number %d",&i);
    scanf("the fifth number %d",&j);
    printf("sum of five numbers =%d",f+g+h+i+j);
    return 0;
}